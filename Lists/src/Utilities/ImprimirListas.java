package Utilities;

public class ImprimirListas {
    public static void imprimirListaDouble(double[] lista) {
        for (double j : lista) {
            System.out.println(j);
        }
    }

    public static void imprimirListaInt(int[] lista) {
        for (int j : lista) {
            System.out.println(j);
        }
    }

    public static void imprimirListaString(String[] lista) {
        for (String j : lista) {
            System.out.println(j);
        }
    }
}
